    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String username = jTextField1.getText();
        String firstname = jTextField2.getText();
        String surname = jTextField4.getText();
        String password = jTextField3.getText();
        String mail = jTextField5.getText();
        String notes = jTextField6.getText();
        String name = firstname + " " + surname;
        
        boolean co_creator = false;
        boolean tutor = false;
        
        int creator_la = -1;
        int tutor_la = -1;
        
        String rank = jComboBox1.getSelectedItem().toString();
        
        int rank_id = 1;
        
        if(rank == "Admin")
            rank_id = 3;
        else if(rank == "Creator")
        {
            rank_id = 2;
            
            String cr = jComboBox2.getSelectedItem().toString();
            
            for(HashMap.Entry<Integer, LearningApp> entry : session.manager.la.entrySet())
            {
                if(entry.getValue().name == cr)
                {
                    creator_la = entry.getKey();
                    break;
                }
            }
            
            if(creator_la == -1)
            {
                JOptionPane.showMessageDialog(rootPane, "You must select creator LA!", "User Edit", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        else if(rank == "Co-Creator")
        {
            rank_id = 2;
            co_creator = false;
            
            String cr = jComboBox2.getSelectedItem().toString();
            
            for(HashMap.Entry<Integer, LearningApp> entry : session.manager.la.entrySet())
            {
                if(entry.getValue().name == cr)
                {
                    creator_la = entry.getKey();
                    break;
                }
            }
            
            if(creator_la == -1)
            {
                JOptionPane.showMessageDialog(rootPane, "You must select creator LA!", "User Edit", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        else if(rank == "Tutor")
        {
            rank_id = 1;
            tutor = true;
            
            String cr = jComboBox2.getSelectedItem().toString();
            
            for(HashMap.Entry<Integer, LearningApp> entry : session.manager.la.entrySet())
            {
                if(entry.getValue().name == cr)
                {
                    tutor_la = entry.getKey();
                    break;
                }
            }
            
             if(tutor_la == -1)
            {
                JOptionPane.showMessageDialog(rootPane, "You must select tutor LA!", "User Edit", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        
        
         if(name.length() < 1)
        {
            JOptionPane.showMessageDialog(rootPane, "Name must be at least 1 character long!", "User Edit", JOptionPane.ERROR_MESSAGE);
            return;
        }
         
         if(surname.length() < 1)
        {
            JOptionPane.showMessageDialog(rootPane, "Surname must be at least 1 character long!", "User Edit", JOptionPane.ERROR_MESSAGE);
            return;
        }
         
         if(username.length() < 1)
        {
            JOptionPane.showMessageDialog(rootPane, "Username must be at least 1 character long!", "User Edit", JOptionPane.ERROR_MESSAGE);
            return;
        }
         
         if(mail.length() < 1)
        {
            JOptionPane.showMessageDialog(rootPane, "Mail must be at least 1 character long!", "User Edit", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
         if(password.length() < 1)
        {
            JOptionPane.showMessageDialog(rootPane, "Password must be at least 1 character long!", "User Edit", JOptionPane.ERROR_MESSAGE);
            return;
        }
         
         if(session.manager.userExistsAdmin(username) != -1  && session.manager.userExistsAdmin(username) != user_id)
         {
              JOptionPane.showMessageDialog(rootPane, "User with that username already exists!", "User Edit", JOptionPane.ERROR_MESSAGE);
              return;
         }
         
         session.manager.updateUserSettings(user_id, username, name, firstname, surname, mail, password, rank_id, co_creator, tutor, creator_la, tutor_la, notes);
         
         JOptionPane.showMessageDialog(rootPane, "User info successfully updated!");
         
         close();
    }//GEN-LAST:event_jButton1ActionPerformed

