    public OADTurkUserEditUI(SessionInfo ses, int uid) {
        session = ses;
        user_id = uid;
        initComponents();
        
        UserInfo user = session.manager.users.get(user_id);
        
        jTextField1.setText(user.user);
        jTextField2.setText(user.first_name);
        jTextField3.setText(user.password);
        jTextField4.setText(user.surname);
        jTextField5.setText(user.mail);
        jTextField6.setText(user.admin_notes);
        
        String text = "";
        if(user.level == 1)
            text = "User";
        else if(user.level == 2)
            text = "Creator";
        else if(user.level == 3)
            text = "Admin";
        
        if(user.tutor)
            text = "Tutor";
        
        if(user.co_creator)
            text = "Co-Creator";
        
        jComboBox1.setSelectedItem(text);
        
        for(HashMap.Entry<Integer, LearningApp> entry : session.manager.la.entrySet())
        {
            jComboBox2.addItem(entry.getValue().name);
        }
        
        if(user.creator_la != -1)
        {
            jComboBox2.setSelectedItem(session.manager.la.get(user.creator_la).name);
        }
    }

